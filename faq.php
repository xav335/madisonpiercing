							<h2>Est-ce que ça fait mal ?</h2>
							<p>Beaucoup moins que ce que l'on s'imagine. L'acte du perçage est très court 2-3 secondes tout au plus. Dites-vous que vous aurez une sensation de piqure brève mais rien qui vous empêcherez par exemple de vous refaire percer :)</p>
							
							<h2>Peut-on anesthésier ?</h2>
							<p>Non, les anesthésies ne peuvent être pratiquées que par un professionnel du corps médical compétent en la matière. En ce qui concerne les crèmes et autres spray dit anesthésiants, ils n'ont pas une réelle efficacité.</p>
							
							<h2>Est-ce que mon piercing peut s'infecter ?</h2>
							<p>Un piercing est une plaie et comme toute plaie celle-ci peut s'infecter si vous n'y apportez pas les soins et l'attention nécessaire. Il vous faudra le désinfecter pendant une période d'environ 1 mois le matin et le soir, ainsi que respecter les consignes que votre perceur vous aura au préalable communiqué. En cas de doute, sur une sensation, une rougeur ou une gêne, il vous sera conseillé de venir nous le montrer.</p>
							
							<h2>Combien de temps dois-je garder mon piercing ?</h2>
							<p>La cicatrisation d'un piercing est variable en fonction de l'emplacement ( tableau de cicatrisation à la fin du FAQ), des coups et accrochages que celui-ci a pu prendre au cours des premiers temps, de l'état générale de la personne percé. Nous vous conseillons vivement de venir nous voir avant le premier changement afin que nous puissions évaluer si la cicatrisation est terminée ou pas ainsi que de vous le changer une première fois dans des conditions optimales pour vous expliquer la marche à suivre. Ne tentez pas surtout pas de retirer votre piercing avant que celui-ci soit complétement cicatrisé, vous pourriez ne pas réussir à le remettre, vous blesser ou autres désagréments. </p>
							
							<h2>Je suis allergique à beaucoup de choses. Puis-je me faire percer ?</h2>
							<p>
								Oui sans aucun problème. Toutefois, il sera important de nous signaler vos allergies afin que nous puissions adapter notre protocole.<br>
								<ul>
									<li>Si vous êtes allergique au nickel : dans tous les cas les bijoux de première pose sont en titane, matière hypoallergénique (qui provoque peu d'allergie) et bio compatible (résiste totalement aux fluides corporels).</li>
									<li>Si vous êtes allergique à l'iode : à la place d'aseptiser votre peau avec de la Bétadine, nous utiliserons un désinfectant à base de Chlorhexidine 0,5%.</li>
									<li>Si vous êtes allergique au latex : nous avons à notre disposition des gants en vinyle ou en nitrile.</li>
								</ul>
							</p>
							
							<h2>Comment prendre soin de mon nouveau piercing ?</h2>
							<p>La contamination des plaies par des micros organismes se fait le plus souvent par les mains. C'est pourquoi, il est vraiment important que vous ne touchiez pas votre piercing en dehors du temps de soin, où vos mains auront été préalablement lavées. Une feuille d'explication du protocole de soins, vous sera remise pour chaque piercing. Les baignades dans les lacs, rivières, piscines vous seront déconseillées les 6-8 premières semaines. Les personnes faisant des sports ou activité pouvant entrainer des chocs, des coups ou obligeant la mise en place d'un pansement sur le piercing sont sujettes de rencontrer plus de difficultés.</p>
							
							<h2>Faut-il prendre rendez-vous pour se faire percer ?</h2>
							<p>Non, vous pouvez venir vous faire percer avec ou sans rendez-vous.</p>
							
							<h2>Percez-vous les mineurs ?</h2>
							<p>
								Une personne mineure souhaitant se faire percer doit-être :<br/>
								<ul>
									<li>+ 15 ans : obligatoirement accompagnée de son parent (tuteur légal) tous deux munis de leurs pièces d'identité respectives et si besoin du livret de famille.</li>
									<li>- 15 ans : certains piercings peuvent être effectués sur les moins de 15 ans, pour cela nous contacter directement en boutique. La présence du parent (tuteur légal), ainsi que les pièces d'identités respectives vous seront demandés.</li>
								</ul>
							</p>
							
							<h2>Si je tombe enceinte, suis-je obligée de retirer mon piercing ?</h2>
							<p>Tout dépendra de la forme que votre nombril prendra au cours des mois. Si celui-ci reste creux, il suffira de changer le bijou pour un « spécial grossesse » (barre plus longue et matière Bioplast)) qui ne dérangera pas non plus lors des échographies. Par contre, si votre nombril ressort, il est préférable de retirer le bijou.</p>
							
							<h2>Pourquoi le perçage au pistolet est déconseillé ?</h2>
							<p>
								<ul>
									<li>L'élément le plus important à connaitre sur l'utilisation du pistolet et que ce dernier n'est pas stérile. Il passe de client en client avec au mieux un nettoyage à l'alcool…</li>
									<li>La stérilisation du pistolet demeure impossible dès l'instant où l'on prend en compte que le degré de chaleur d'un stérilisateur altère l'appareil.</li>
									<li>Le pistolet est totalement déconseillé pour tous types de piercings ; en dehors des lobes d'oreilles pour lesquels il a été conçu (nous ne vous le conseillons pas pour autant).</li>
									<li>Le concept même de l'appareil est de déclencher un ressort qui provoque une impulsion directement sur le bijou afin d'être inséré dans la peau. Comme le principe d'une agrafeuse.</li>
									<li>Cet acte relativement violent pour les tissus intérieurs entrainera chez la plupart des personnes de sérieux problèmes de cicatrisation.</li>
								</ul>
							</p>
							
							<h2>Quelles sont les précautions à prendre avant et/ou après s'être fait percer ?</h2>
							<p>
								<ul>
									<li>Faites en sorte de bien manger, pour pouvoir avoir l'énergie nécessaire et éviter la sensation de « coup de mou ».</li>
									<li>Eviter la consommation d'alcool ou de drogue 24h00 auparavant et environ 48h00 après le piercing. Ces substances ont pour effet de fluidifier le sang, il coagulera moins vite et votre cicatrisation sera affectée avant même d'avoir commencé.</li>
									<li>Certaines maladies affectent vos défenses immunitaires et vous rendent plus sujet aux infections. Si vous avez des soucis de santé, des problèmes de peau ou tout autres symptomatiques qui pourrait faire le cas d'une contre-indication pour un piercing, il est préférable de vous adresser auprès du corps médical compétent afin de s'assurer que vous pouvez ou pas effectuer un piercing .</li>
								</ul>
